/**
 * Constantes.
 * Espaço na memória onde pode-se armazenar valores. Constantes não podem ter seus valores alterados.
 */

// declarando e inicializando constante.
const nome = 'Wellington Felix';

console.log(nome);

const valor01 = 5;
const valor02 = 5;
const resultado = valor01 * valor02;

console.log(resultado);

/**
 * Observações:
 *  Não utilizar var para criar constantes;
 *  Não redeclarar a mesma constante com const;
 *  Não declarar uma constante e depois inicializá-la;
 *  Sempre que possível, utilizar const ao invés de let para manter o valor original armazenado;
 */